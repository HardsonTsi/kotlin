fun main() {
    var speedOfLight = 186_000

    // argumented assignments operators

    // speedOfLight = speedOfLight + 282
    speedOfLight += 282

    // speedOfLight = speedOfLight - 282
    speedOfLight -= speedOfLight

    // speedOfLight = speedOfLight * 282
    speedOfLight *= 282

    // speedOfLight = speedOfLight / 282
    speedOfLight /= 282

    // speedOfLight = speedOfLight % 3
    speedOfLight %= 3

    // increment and decrements operators

    var apples = 5
    apples++
    println(apples)


}