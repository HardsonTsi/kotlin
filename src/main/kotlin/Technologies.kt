/*
*  A set is an un-ordered collection of unique elements.
*  Upon creating or adding elements to set, the set automatically filters out any duplicates and always returns
*  unique collection
* */

fun main() {

    var obseleteTech = setOf("Rolodex", "Phonograph", "Videocassette recorder", "Video projector", "Rolodex")

    print(obseleteTech)
}